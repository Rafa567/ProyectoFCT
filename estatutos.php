                                direcci&oacute;n.</em><br />
                              <em>Tendr&aacute;n &eacute;stos las siguientes misiones:</em></p>
                            <p class="Estilo47"><em>1&ordm;.- Hacerse cargo de todo el material deportivo de la
                                secci&oacute;n que corresponda.</em></p>
                            <p class="Estilo47"><em>2&ordm;.- Organizar y dirigir todas las secciones deportivas,
                                competiciones, entrenamientos, etc.</em></p>
                            <p class="Estilo47"><em>3&ordm;.- Dar cuenta y pedir a la Junta, la adquisici&oacute;n de
                                material, las reparaciones y bajas de los mismos cuando esto sea necesario.</em></p>
                            <p align="center" class="Estilo47"><strong><u>TITULO CUARTO</u></strong><br />
                              <strong>De la Econom&iacute;a</strong></p>
                            <p align="center" class="Estilo47"><strong>CAPITULO PRIMERO</strong><br />
                              <strong>Recursos econ&oacute;micos</strong></p>
                            <p class="Estilo47"><strong><em>Art. 46.&mdash;</em></strong><em>Los recursos
                                econ&oacute;micos de la Sociedad estar&aacute;n constituidos:</em></p>
                            <p class="Estilo47"><em>1&ordm;.- Por la cuota de los socios.</em></p>
                            <p class="Estilo47"><em>2&ordm;.- Por los beneficios que se obtengan de los recreos y
                                deportes en las instalaciones de la Sociedad.</em></p>
                            <p class="Estilo47"><em>3&ordm;.- Por las subvenciones que reciba la Sociedad de cualquier
                                Entidad Oficial</em></p>
                            <p class="Estilo47"><em>4&ordm;.-&nbsp; Por cualquier otro ingreso no especificado en los
                                anteriores.</em></p>
                            <p align="center" class="Estilo47"><strong>CAPITULO SEGUNDO</strong><br />
                              <strong>Sistema de Contabilidad</strong></p>
                            <p class="Estilo47"><strong><em>Art. 47.&mdash;</em></strong><em>La Sociedad desarrolla
                                contabilidad por el sistema de partida doble, con los libros necesarios para ello.</em>
                            </p>
                            <p align="center" class="Estilo47"><strong><u>TITULO QUINTO</u></strong><br />
                              <strong>Del r&eacute;gimen interior y personal</strong></p>
                            <p align="center" class="Estilo47"><strong>CAPITULO PRIMERO</strong><br />
                              <strong>Del funcionamiento de la Sociedad</strong></p>
                            <p class="Estilo47"><strong><em>Art. 48.&mdash;</em></strong><em>El desarrollo de las
                                actividades deportivas, competiciones y festejos, habr&aacute; de ajustarse a las normas
                                de una sana moral y que su celebraci&oacute;n sea previamente conocida por la Junta
                                Directiva quien dispondr&aacute; su organizaci&oacute;n.</em></p>
                            <p class="Estilo47"><strong><em>Art. 49.&mdash;</em></strong><em>El empleo de material
                                deportivo o recreativo por los socios estar&aacute; de acuerdo con las instrucciones que
                                establezca la Junta Directiva.</em></p>
                            <p class="Estilo47"><strong><em>Art. 50.&mdash;</em></strong><em>Cuando a juicio de la
                                Sociedad se considere que se ha llegado a un grado de eficiencia, en cualquier rama del
                                deporte, que permita concurrir a competiciones locales, regionales o nacionales con
                                otras sociedades, se solicitar&aacute; de las Autoridades deportivas correspondientes la
                                inclusi&oacute;n en la Federaci&oacute;n regional de que se trate.</em></p>
                            <p align="center" class="Estilo47"><strong>CAPITULO SEGUNDO</strong><br />
                              <strong>Del personal de servicio</strong></p>
                            <p class="Estilo47"><strong><em>Art. 51.&mdash;</em></strong><em>Todo el personal de
                                servicio est&aacute; a la disposici&oacute;n de los socios para atenderles en el
                                interior de lo locales de que disponga la Sociedad, sin que puedan utilizarlos en
                                encargos servicio de otro orden, lo har&aacute;n por intermedio del personal nombrado
                                para ello.</em></p>
                            <p class="Estilo47"><strong><em>Art. 52.&mdash;</em></strong><em> El personal de servicio
                                vestir&aacute; siempre el traje que se disponga, debiendo estar todo &eacute;l, en
                                cualquier momento, limpio y aseado.</em><br />
                              <em>El personal queda obligado a mantener la parte destinada a ello en el perfecto estado
                                de limpieza, debiendo tener en cuenta al servir, las normas para el mejor orden de los
                                locales y conservaci&oacute;n del mobiliario y efectos.</em></p>
                            <p class="Estilo47"><strong><em>Art. 53.&mdash;</em></strong><em>Las quejas que de cualquier
                                orden los socios puedan tener de servicios o del personal que los atiendan, las
                                pondr&aacute;n en conocimiento Secretario de la Junta Directiva. En cualquier caso queda
                                terminantemente prohibido toda discusi&oacute;n con el personal a que afecte.</em></p>
                            <p align="center" class="Estilo47"><strong><u>TITULO SEXTO</u></strong><br />
                              <strong>De las Juntas Generales</strong></p>
                            <p class="Estilo47"><strong><em>Art. 54.&mdash;</em></strong><em>Las Juntas Generales pueden
                                ser Ordinarias y Extraordinarias. Para la celebraci&oacute;n de dichas juntas se
                                convocar&aacute;n con diez d&iacute;as de anticipaci&oacute;n en el Tabl&oacute;n de
                                Anuncios o a los socios individualmente, por medio de circulares &nbsp;haciendo constar
                                en las mismas el orden del d&iacute;a.</em></p>
                            <p class="Estilo47"><strong><em>Art. 55.&mdash;</em></strong><em>Las Juntas Generales
                                ordinarias deben celebrarse en los meses de Junio y Diciembre de cada a&ntilde;o; en
                                esta &uacute;ltima y en el a&ntilde;o que corresponda se renovar&aacute; la Junta
                                Directiva. Las extraordinarias se celebrar&aacute;n siempre que lo acuerde la Junta
                                Directiva.</em></p>
                            <p class="Estilo47"><strong><em>Art. 56.&mdash;</em></strong><em>Las Juntas Generales
                                ordinarias se tendr&aacute;n por v&aacute;lidamente constituidas cuando asistan a ella
                                80 socios de n&uacute;mero: y las extraordinarias cuando asistan la mitad m&aacute;s uno
                                de los socios de n&uacute;mero, en primera convocatoria. Cuando no se constituyeran por
                                falta de n&uacute;mero de socios, se volver&aacute; a citar con una hora de intervalo,
                                en segunda convocatoria y ser&aacute;n v&aacute;lidos todos lo acuerdos que se adopten
                                sea cualquiera el n&uacute;mero de asistentes.</em></p>
                            <p class="Estilo47"><strong><em>Art. 57.&mdash;</em></strong><em>Son atribuciones de la
                                Junta General ordinaria:</em><br />
                              <em>1&ordm;.- El examen, censura y aprobaci&oacute;n de cuentas anuales.</em></p>
                            <p class="Estilo47"><em>2&ordm;.- Fijar la cuota de ingreso y mensual de los socios y
                                acordar el pago de cuotas extraordinarias.</em></p>
                            <p class="Estilo47"><em>3&ordm;.- Deliberar sobre toda clase de asuntos que proponga la
                                Junta Directiva o los socios, siempre que hayan sido incluidos en el orden del
                                d&iacute;a.</em></p>
                            <p class="Estilo47"><strong><em>Art. 58.&mdash;</em></strong><em>Ser&aacute; necesaria la
                                celebraci&oacute;n de Junta General extraordinaria para:</em><br />
                              <em>1) Acordar sobre la adquisici&oacute;n, enajenaci&oacute;n y gravamen de bienes
                                inmuebles.</em><br />
                              <em>2) Deliberar sobre la contrataci&oacute;n de empr&eacute;stito.</em><br />
                              <em>3) Estudiar y resolver las modificaciones del Reglamento.</em><br />
                              <em>4) Liquidar y disolver la Sociedad y deliberar sobre las bases de estas
                                operaciones.</em></p>
                            <p class="Estilo47"><strong><em>Art. 59.&mdash;</em></strong><em>Corresponde al Presidente
                                privativamente, la facultad de presidir las Juntas Generales y dirigir los debates, una
                                vez que hayan hecho uso de la palabra tres socios en pro de la proposici&oacute;n que se
                                debata y otros tres en contra; el Presidente podr&aacute; dar por terminada la
                                discusi&oacute;n. Pertenece, sin embargo a sus facultades discrecionales, seg&uacute;n
                                la importancia del asunto y las circunstancias del caso, dar al debate m&aacute;s o
                                menos amplitud.</em></p>
                            <p class="Estilo47"><em>Las Juntas Generares resolver&aacute;n todo los asuntos por
                                mayor&iacute;a absoluta de votos que se emitan; en primera convocatoria.</em><br />
                              <em>Si no se obtuviera esta mayor&iacute;a, en segunda votaci&oacute;n, ser&aacute;
                                suficiente para dar validez a los acuerdos de que se traten, el criterio que m&aacute;s
                                votos haya obtenido.</em></p>
                            <p class="Estilo47"><em>Las votaciones ser&aacute;n:</em><br />
                              <em>1&ordm;.- Por aclamaci&oacute;n.</em><br />
                              <em>2&ordm;.- Nominales.</em><br />
                              <em>3&ordm;.- Por papeletas, y</em><br />
                              <em>4&ordm;.- Por bolas blancas y negras.</em></p>
                            <p class="Estilo47"><em>Cuando se trate de elecci&oacute;n de cargos, la votaci&oacute;n
                                ser&aacute; por papeletas.</em><br />
                              <em>Ser&aacute;n nominales las votaciones cuando no se trate de la elecci&oacute;n de
                                cargos ni de asuntos que afecten al decoro de la Sociedad o de alg&uacute;n
                                socio.</em><br />
                              <em>Ser&aacute;n por bolas blancas y negras, cuando se trate de: asuntos que afecten al
                                decoro de la Sociedad o de alg&uacute;n socio.</em></p>
                            <p align="center" class="Estilo47"><strong><u>TITULO SEPTIMO</u></strong><br />
                              <strong>Del Consejo Consultivo</strong></p>
                            <p class="Estilo47"><strong><em>Art. 60.&mdash;</em></strong><em>La Sociedad tendr&aacute;
                                un Consejo Consultivo integrado por todos los socios que hayan desempe&ntilde;ado dentro
                                de la Sociedad los cargos &nbsp;de Presidente y Vicepresidente y que presidir&aacute; el
                                m&aacute;s antiguo de los Expresidentes.</em></p>
                            <p class="Estilo47"><strong><em>Art. 61.&mdash;</em></strong><em>AI Consejo o corresponde
                                recoger la direcci&oacute;n y el gobierno de la Sociedad cuando por dimisi&oacute;n o
                                cesaci&oacute;n de una Directiva sin que acepte la elegida, se encuentre la Sociedad sin
                                Autoridad que la rija, tomando entonces, para el restablecimiento de la marcha normal de
                                la misma, aquellas medidas que estimen convenientes.</em></p>
                            <p class="Estilo47"><strong><em>Art. 62.&mdash;</em></strong><em>Ser&aacute;
                                obligaci&oacute;n de este Consejo dar dictamen a la Junta General y a la Junta
                                Directiva, cada vez que &eacute;stas lo soliciten, sobre aquellos asuntos que se le
                                consulten. El dict&aacute;men del Consejo Consultivo no vincular&aacute; a a la Junta
                                Directiva ni a la Junta General.</em></p>
                            <p class="Estilo47"><em>&nbsp;</em></p>
                            <p align="center" class="Estilo47"><strong><u>TITULO OCTAVO</u></strong><br />
                              <strong>De la disoluci&oacute;n de la Sociedad</strong></p>
                            <p class="Estilo47"><strong><em>Art. 63.&mdash;</em></strong><em>La Junta General que
                                acuerde la disoluci&oacute;n de la Sociedad, fijar&aacute; las bases a que estas
                                operaciones han de acomodarse. Designar&aacute; las personas que &nbsp;han de llevarlas
                                a cabo &nbsp;y determinar&aacute; sus facultades.</em></p>
                            <p class="Estilo47"><em>En cumplimiento del art&iacute;culo 3.&deg;, apartado segundo,
                                n&uacute;mero 9 de la Ley de Asociaciones de 24 de diciembre de 1964, se establece que
                                el patrimonio social existente ser&aacute; destinado necesariamente a obras
                                ben&eacute;ficas y culturales de la Ciudad.</em></p>
                            <p align="center" class="Estilo47"><em>&nbsp;</em></p>
                            <p align="center" class="Estilo47"><strong><u>TITULO NOVENO</u></strong><br />
                              <strong>Del patrimonio de la Sociedad</strong></p>
                            <p class="Estilo47"><strong><em>Art. 64.&mdash;</em></strong><em> El cumplimiento del
                                art&iacute;culo 3&ordm;, apartado 2.&deg;, n&uacute;mero 8, de la Ley 191/1964 de
                                Asociaciones, se hace constar que el patrimonio de esta Sociedad, al comenzar su
                                funcionamiento asciende a 60.000 pesetas incluido en ello bienes inmuebles, mobiliarios,
                                enseres y met&aacute;lico.</em><br />
                              <em>Se se&ntilde;ala como l&iacute;mites del presupuesto anual las cantidades de 80.000 a
                                95.000 pesetas.</em></p>
                            <p class="Estilo47"><em>&nbsp;</em></p>
                            <p align="center" class="Estilo47"><strong><em>Visados, en virtud de modificaci&oacute;n
                                  aprobada, a tenor de lo prevenido en el art&iacute;culo 3.5 - en relaci&oacute;n con
                                  el C. 4 - de la Ley de 24 de Diciembre de 1964, por resoluci&oacute;n de 21 de
                                  Septiembre de 1982.</em></strong><br />
                              <strong><em>Madrid, 21 de Septiembre de 1982</em></strong><br />
                              <strong><em>El Secretario, Agust&iacute;n D&iacute;az Ostos</em></strong></p>
                            <span class="Estilo47"><strong><em>V&ordm; B&ordm; El Presidente, Miguel Osuna
                                  Saavedra</em></strong></span>
